        <p>
        &nbsp;&nbsp;&nbsp;&nbsp; Melphas and Nahir began to tire of the simplicity of their realms after eons with their creations. Knowing that alone 
        each would only be able to create variants of the same creation, the two brothers came together to create a third plane. This was the plane of mortals. 
        Unlike each of their original creations, the creatures of the mortal realm were not made of undying flesh. Mortals would eventually fall to death from 
        age, but their immortal soul would stay within the realm and be reborn.
	    <br>
	    &nbsp;&nbsp;&nbsp;&nbsp; Being children of both Melphas and Nahir, the mortals were capable of more than just darkness or light. Each being was a mixture 
	    of the two and often struggled in balancing its two halves. Enthralled with the success of their creation, the two gods created variations of the 
	    original race of mortals and shaped a world that matched their duality.
	    <br>
	    &nbsp;&nbsp;&nbsp;&nbsp; The mortals lived in peace and grew in number as they spread across the world created for them. Despite differences between 
	    them, there was acceptance.
