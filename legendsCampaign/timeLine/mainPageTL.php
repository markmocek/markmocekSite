        </div>
        <p>
        &nbsp;&nbsp;&nbsp;&nbsp; The god brothers Melphas and Nahir were the first beings to exist in the empty void. Desiring to create 
        something to give their endless existence a purpose and interest, the two each created a plane of existence.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Melphas created a dark plane he named Hell. Melphas poured some of his own power into his first five creations 
        creating Hell's Demon Lords. Each lord embodied aspects of an existence in Hell. The five great demons were Deumos, the lord of lust; 
        Haures, the lord of hate; Raum, the lord of lies; Shax, the lord of disease; and Uphir, the lord gluttony. Melphas then created hordes 
        of creatures for the demon lords to afflict and torture into twisted images of themselves creating lesser demons.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Nahir created a plane of light referred to as Heaven. Like Melphas, Nahir poured some of his power into his first 
        creations, the Archangels. The first Arch Angel created was Adonael, the angel of purity. In addition to being the first creation of Nahir, 
        Adonael was also bestowed with more of Nahir's essence than his fellow Archangels Imperious, the angel of valor; Guriel, the angel of life; 
        Karael, the angel of peace; Hadriel, the angel of mercy; and Sidriel, the angel of justice. To accompany his Archangels, Nahir created a 
        host of lesser angels.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; The two brothers had created planes that spoke to the nature of the creator. Heaven, a world of peace and order. 
        Hell, a world of chaos and strife.
        </p>
