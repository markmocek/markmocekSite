    <!-- **************************Event THREE****************************** -->
    <div class="wrapper0 mt10">
        <div class="title2">
            <a id="E3"><b><u>Corruption of the Mortal Plane: Year 834 Age of Fahren</u></b></a>
        </div>
        
        <p>
        &nbsp;&nbsp;&nbsp;&nbsp; After years of watching the mortals, Melphas started growing impatient. He wanted to see the darkness in the mortals. Melphas 
        wanted to see the power he placed inside of them, but the mortals seemed to actively strive for the qualities bestowed in them by Nahir. Melphas wondered 
        if his brother had not been fair in the balance of their creation. Why would Nahir place more of his essence in the mortal and create an imbalance if the 
        purpose of this realm was an equilibrium of their two powers? Melphas believed the only reason Nahir would cause an imbalance was to create a shift in 
        power. With the combined efforts of Heaven and the souls of  mortal world Terra, Nahir would be powerful enough to destroy Melphas. Enraged, Melphas 
        confronted Nahir.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Nahir admitted to placing more of his essence into the creation of the mortals than was agreed upon, although Nahir did not do 
        so in order to become more powerful. Nahir believed that the mortal races would not prosper with an equal disposition for light and dark. In order to 
        move beyond a small group of people endlessly killing one another and fill their world, light needed to triumph.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Melphas was not convinced by his brother's explanation. Knowing the only way he could overcome Nahir was by harnessing the power 
        of the souls they worked together to create, Melphas linked the mortal world Terra to Hell. Unfortunately, the link was not strong and could only take souls 
        from Terra after the death of one of its inhabitants. Even then the soul had to be primarily dark, limiting Melphas to the mortals who had lived 
        lives more akin to his own creations. Nahir sensed the link that Melphas had made to Terra. Knowing what Melphas planned on doing with their joint creation, 
        Nahir opened his own link to Terra.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; With the links to Terra established, the reincarnation process was destroyed. Souls were no longer able to reenter the cycle 
        due to the links that pulled them upon death. Melphas once again felt cheated. Since the souls were comprised of more light than dark, Nahir was collecting 
        more souls than Melphas. Seeking a way to tip the balance back in his favor, Melphas turned his attention to the city of Maura.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Maura was a small city where Melphas's darkness was most prevalent and the people there often indulged in their dark desires. Melphas came 
        to the people and told them that Nahir was planning to destroy him, and once he was gone Nahir would purge the world of the pleasures they had come to enjoy. 
        Melphas told them the only way to prevent this was to aid him in stopping Nahir. To better prepare for the tasks that would arise, Melphas endowed the people 
        with power from his realm of darkness. The people became stronger, but also acquired demonic features similar to those of Melphas's original creations. 
        Melphas called his mortal hybrids Tieflings and sent them to spread his darkness.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Nahir was dismayed to find what Melphas had done to the mortals of Maura. He knew that the new race of Tieflings would be a threat 
        to not only the other races, but also to himself should Melphas become strong enough. In light of this, Nahir appeared to the city of Anora. The people of 
        this city were exceptionally zealous and eager to lend aid to the Lord of Light. Nahir bestowed angelic power within the people, creating the first Aasimar.
        </p>
