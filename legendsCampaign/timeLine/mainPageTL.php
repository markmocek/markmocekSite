    <div class="wrapper0 mt10">
        <div class="title2">
            <a id="E4"><b><u>The First War of Mortals: Year 0 - 49 Age of Marsil</u></b></a>
        </div>
        
        <p>
        &nbsp;&nbsp;&nbsp;&nbsp; War broke out between the followers of Melphas and those of Nahir. Cities were burned to ash as the magi of Melphas's army set fire to 
        their foes. Nahir's forces massacred thousands of people as they attempted to rid the world of people influenced by the darkness. Those who did not join a side 
        were accused of sympathizing with the enemy and were killed by both factions.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Those feeling no connection to either side sought refuge from the conflict. The elves, the vast majority of which did not participate in 
        the war, built themselves a sanctuary called Sindarin to escape the war. As news of Sindarin spread to groups seeking an escape, the population of the city grew in 
        both number and diversity. Dwarves built vast networks for their kind beneath the foundation of the city while groups of gnomes, humans, and halflings blended 
        into the elven populous.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; The war continued for years as both sides suffered losses of their god's chosen. Both factions were concerned when they discovered that the 
        Tieflings and Aasimar were unable to produce new generations of their kind. All attempted breedings resulted in the birth of more or less average humans. Realizing 
        that time was not on their side, due to the limited and dwindling number of Tiefling or Aasimar, paranoia set in. Each side feared the other would sway the neutral 
        inhabitants of Sindarin, which had a rapidly increasing population, to their side and decimate the other. In their apprehension, both sides attacked the city.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; The initial strike devastated the city. Unprepared for such an assault, almost half of the city's surface populace died. Elven magi began 
        erecting barriers and wards to protect themselves from another attack while the surviving surface population was moved into the underground city of the dwarves. 
        After suffering such losses, the city would fare poorly against a continued attack from both sides. Despite the severity of the circumstance, the people of 
        Sindarin refused to subdue and join a group that had murdered so many of their people. A council, made up of a few members of each race, formed and elected to beg 
        the race of dragons and dragonborn that lived in the harsh elements of Ormarr for support.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; The people of Sindarin waited for an answer from Ormarr as the war they had tried so hard to escape was waged right on top of them. The 
        armies of Nahir and Melphas quickly returned to their fight with each other while simultaneously trying to finish off the destroyed city. Every one of Sindarin's 
        populous had either been killed or forced to hide within the dwarven tunnels. Those who did not die by the hands of the two warring armies started succumbing to 
        starvation and sickness. As the weeks turned to months, the recently formed council began to fear that their embassy never made it to Ormarr. Their numbers dwindled 
        to nearly a tenth of their original size when the Sindarin magi first heard word from the magi of Ormarr. The few of the remaining people that could fight prepared 
        for battle as the reinforcements from Ormarr drew near.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Once the Ormarrians arrived, the tides of battle quickly turned. The Sindarin people emerged from their sanctity beneath the ruins of Sindarin 
        to find a multitude of dragons and dragonborn descending on the city. Dragonborn warriors began driving back foot soldiers while dragonborn magi assaulted groups of 
        warriors belonging to Nahir and Melphas alike. While the dragonborn focused on those within the city, the dragons dealt with the armies on the outskirts. The massive 
        beasts cleaved hordes of warriors with their claws. Those who were not killed by claws were bombarded with an assortment of fire, ice, lightning, acid, and poison.
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; The dragon and dragonborn forces extinguished the two armies in a matter of days, leaving those who managed to survive fleeing and hiding. 
        Unfortunately, the war had left the city of Sindarin in ruins. The remaining people left the city to rebuild somewhere new. Grateful to the dragons and dragonborn, 
        the council offered seats on the council and a hope for continued cooperation. Some of the dragons and dragonborn agreed and traveled with the people to help build 
        their new home. Most of them, however, chose to return to Ormarr. Eventually, the former people of Sindarin reached a location they believed would be suitable to 
        rebuild. Together the races constructed the city of Peryn.
        </p>
